class PredictionSummary(BaseModel):
    """Slim prediction summary embedded in a fixture response."""

    model_config = ConfigDict(from_attributes=True)

    id: str
    home_win_prob: float
    draw_prob: Optional[float] = None
    away_win_prob: float
    predicted_home_score: Optional[float] = None
    predicted_away_score: Optional[float] = None
    confidence: float
    disclaimer: str = _SIMULATION_DISCLAIMER
    is_simulation: bool = True


class ResultSummary(BaseModel):
    """Score summary embedded in a fixture response."""

    home_score: int
    away_score: int
    home_score_ht: Optional[int] = None
    away_score_ht: Optional[int] = None
    winner: Optional[str] = None  # "home" | "away" | "draw"


class FixtureItem(BaseModel):
    """A single fixture row as returned by the /fixtures endpoints."""

    id: str
    league_id: str
    league_name: Optional[str] = None
    league_slug: Optional[str] = None
    home_team_id: str
    home_team_name: Optional[str] = None
    home_team_slug: Optional[str] = None
    away_team_id: str
    away_team_name: Optional[str] = None
    away_team_slug: Optional[str] = None
    external_id: Optional[str] = None
    status: str
    scheduled_at: datetime
    venue: Optional[str] = None
    round_name: Optional[str] = None
    matchday: Optional[int] = None
    result: Optional[ResultSummary] = None
    prediction: Optional[PredictionSummary] = None


class UpcomingFixturesResponse(BaseModel):
    """Response envelope for /fixtures/upcoming."""

    count: int
    disclaimer: str = _SIMULATION_DISCLAIMER
    fixtures: List[FixtureItem]


class TodayFixturesResponse(BaseModel):
    """Response envelope for /fixtures/today."""

    date: str
    count: int
    disclaimer: str = _SIMULATION_DISCLAIMER
    fixtures: List[FixtureItem]


class ResultsResponse(BaseModel):
    """Response envelope for /fixtures/results."""

    days: int
    count: int
    disclaimer: str = _SIMULATION_DISCLAIMER
    fixtures: List[FixtureItem]


class WeeklySummaryItem(BaseModel):
    """One team's performance entry in the weekly summary."""

    team_id: str
    team_name: Optional[str]
    team_slug: Optional[str]
    matches_played: int
    wins: int
    draws: int
    losses: int
    goals_for: int
    goals_against: int
    goal_difference: int
    points: int


class WeeklySummaryResponse(BaseModel):
    """Response envelope for /fixtures/results/weekly-summary."""

