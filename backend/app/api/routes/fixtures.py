"""
Fixtures API Routes
====================
All routes in this module read ONLY from the local PostgreSQL database.
No external API calls are made.  Data is kept fresh by the background
``task_sync_matches`` Celery task which runs every 5 minutes.

Endpoints
---------
GET /fixtures/upcoming              – upcoming matches (+ predictions)
GET /fixtures/today                 – today's matches
GET /fixtures/results               – finished matches with scores (?days=7)
GET /fixtures/results/weekly-summary – weekly winners/losers stats

SIMULATION / EDUCATIONAL DISCLAIMER
-------------------------------------
All probability estimates provided on this platform are produced by a
statistical model for simulation and educational research purposes only.
They do NOT constitute financial, betting, or investment advice of any kind.
"""
